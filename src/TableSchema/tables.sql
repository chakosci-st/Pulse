CREATE OR REPLACE TRIGGER NPITRACK.tr_bi_project
   BEFORE INSERT
   ON NPITRACK.PROJECTS    FOR EACH ROW
DECLARE
   v_prefix_sequence   VARCHAR2 (14) := 'SEQ_PROJECTNO_';
   v_sequencename      VARCHAR2 (30);
   v_zerofill          INT := 6;
   v_idyearformat      VARCHAR2 (4) := 'YYYY';
   v_idformat          VARCHAR2 (100) := '{YEAR}-{ID}';
   v_value             INT;
BEGIN
   IF :new.projectno IS NULL
   THEN
      SELECT seq_nextval_on_demand (v_prefix_sequence || :new.PLANTCODE)
        INTO v_value
        FROM DUAL;

      SELECT :new.PLANTCODE || '-'
             || REPLACE (
                   REPLACE (v_idformat,
                            '{YEAR}',
                            TO_CHAR (SYSDATE, v_idyearformat)),
                   '{ID}',
                   LPAD (v_value, v_zerofill, 0))
        INTO :new."PROJECTNO"
        FROM DUAL;
   END IF;
END;
/


